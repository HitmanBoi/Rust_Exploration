fn main() {
    let x = 255.0 ;
    let y = 10.0;

    let add1 = x + y ;
    println!("{} + {} = {}",x,y,add1); //Addition
    
    let sub1 = x - y ;
    println!("{} - {} = {}",x,y,sub1); // Subtraction 

    let mul1 = x * y ;
    println!("{} x {} = {}",x,y,mul1); // Multiplication

    let div_que = x / y ;
    println!("{} / {} = {}",x,y,div_que); // Division for quotient

    let div_rem = x % y ;
    println!("{} % {} = {}",x,y,div_rem); // Division for remainder
    
    
    
    
}
